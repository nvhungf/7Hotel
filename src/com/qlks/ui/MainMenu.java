        public boolean check(){
        PhongDao pd = new PhongDao();
        Phong phong = new Phong();
        ArrayList<Phong> dsph = pd.docPhong();
            for (Phong ph : dsph){
                if(txtTenPhong.getText().length()==0){
                    JOptionPane.showMessageDialog(this, "Ko để trống tên phòng");
                    txtTenPhong.requestFocus();
                    return true ;            
                }
                if(txtTenPhong.getText().trim().equalsIgnoreCase(ph.getMaPhong().trim())){
                    JOptionPane.showMessageDialog(this, "Trùng tên phòng");
                    txtTenPhong.requestFocus();
                    return true ;
                }
            }
            if(txtLoaiPhong.getText().length()==0){
                    JOptionPane.showMessageDialog(this, "Ko để trống loại phòng");
                    txtLoaiPhong.requestFocus();
                    return true ;            
                }
            if(txtLoaiPhong.getText().equalsIgnoreCase("Deluxe")!=true
                    && txtLoaiPhong.getText().equalsIgnoreCase("Standard")!=true
                    && txtLoaiPhong.getText().equalsIgnoreCase("Superior")!=true
                    && txtLoaiPhong.getText().equalsIgnoreCase("Suite")!=true){
                        JOptionPane.showMessageDialog(this, "Nhập 1 trong 4 loại phòng: "
                        + "Deluxe, Standard, Superior, Suite");
                        txtLoaiPhong.requestFocus();
                        return true;
                }
            if(txtGiaThueNgay.getText().length()==0){
                    JOptionPane.showMessageDialog(this, "Ko để trống giá thuê ngày");
                    txtGiaThueNgay.requestFocus();
                    return true ;            
                }
            if(txtGiaThueNgay.getText().trim().matches("-?\\d+(\\.\\d+)?")!=true){
                JOptionPane.showMessageDialog(this, "Giá phòng không được nhập chữ");
                txtGiaThueNgay.requestFocus();
                return true;
            }
            if(txtGiaThueGio.getText().length()==0){
                    JOptionPane.showMessageDialog(this, "Ko để trống giá thuê giờ");
                    txtGiaThueGio.requestFocus();
                    return true ;            
                }
            if(txtGiaThueGio.getText().trim().matches("-?\\d+(\\.\\d+)?")!=true){
                JOptionPane.showMessageDialog(this, "Giá phòng không được nhập chữ");
                txtGiaThueGio.requestFocus();
                return true;
            }
            if(txtGhiChu.getText().trim().equalsIgnoreCase("Đã thuê")!=true && 
                    txtGhiChu.getText().trim().equalsIgnoreCase("Chưa thuê")!=true){
                JOptionPane.showMessageDialog(this, "Nhập tình trạng: Đã thuê hoặc chưa thuê");
                txtGhiChu.requestFocus();
                return true;
            }
            return false;
    }
    public boolean checkThuePhong(){
        if(txtTenKHThuePhong.getText().length()==0){
                    JOptionPane.showMessageDialog(this, "Ko để trống Tên KH");
                    txtTenKHThuePhong.requestFocus();
                    return true ;            
                }
        if(txtCMNDThuePhong.getText().length()==0){
                    JOptionPane.showMessageDialog(this, "Ko để trống CMND");
                    txtCMNDThuePhong.requestFocus();
                    return true ;            
                }
        if(txtCMNDThuePhong.getText().trim().matches("-?\\d+(\\.\\d+)?")!=true){
                JOptionPane.showMessageDialog(this, "Nhập đúng CMND");
                txtCMNDThuePhong.requestFocus();
                return true;
            }
        if(txtCMNDThuePhong.getText().length()!=9 && txtCMNDThuePhong.getText().length()!=12){
                JOptionPane.showMessageDialog(this, "Nhập đúng CMND");
                txtCMNDThuePhong.requestFocus();
                return true;
            }
        return false;
    }
