    private void btnThuePhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThuePhongActionPerformed
        // TODO add your handling code here:
        if(checkThuePhong()){
            return;
        }
        PhongDao phongDao = new PhongDao();
        PhongDaThueDao pd = new PhongDaThueDao();
        PhongDaThue pdt = new PhongDaThue();
        KhachHangDao khd = new KhachHangDao();
        KhachHang kh = new KhachHang();
//        ArrayList<PhongDaThue> pdtl = new ArrayList<PhongDaThue>();
        pdt.setMaPhong(txtMaThuePhong.getText());
        pdt.setLoaiPhong(txtLoaiThuePhong.getText());
        pdt.setTenKH(txtTenKHThuePhong.getText());
        pdt.setCmnd(txtCMNDThuePhong.getText());
        pdt.setSoLuong(txtGioThue.getText());
        if (rdoTheoNgay.isSelected()) {
            pdt.setGiaPhong(Double.parseDouble(txtGiaThuePhong.getText()));
            pdt.setLoaiThue("Theo ngày");
        } else if (rdoTheoGio.isSelected()) {
            pdt.setGiaPhong(Double.parseDouble(txtGiaGio.getText()));
            pdt.setLoaiThue("Theo giờ");
        }

        pdt.setNgayThue(txtNgayThue.getText());

        kh.setCmnd(txtCMNDThuePhong.getText());
        kh.setTenKH(txtTenKHThuePhong.getText());
        if (pd.luuPhongDaThue(pdt) > 0) {
            if (khd.luuKhachHang(kh) > 0) {
                if (phongDao.capNhatTinhTrangPhong(pdt.getMaPhong()) > 0) {
                    JOptionPane.showMessageDialog(null, "Cập nhật tình trạng thành công");
                    resetForm();
                } else {
                    JOptionPane.showMessageDialog(null, "Cập nhật tình trạng thất bại");
                }
                fillToTable();
                JOptionPane.showMessageDialog(null, "Lưu thành công");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Lưu thất bại");
        }
    }//GEN-LAST:event_btnThuePhongActionPerformed

