    public void suaPhong() {
        PhongDao pd = new PhongDao();
        Phong phong = new Phong();
        phong.setMaPhong(txtTenPhong.getText().trim());
        String lp = txtLoaiPhong.getText().trim();
        if (lp.equalsIgnoreCase("Deluxe")) {
            phong.setMaLoai("DLX");
        } else if (lp.equalsIgnoreCase("Standard")) {
            phong.setMaLoai("STD");
        } else if (lp.equalsIgnoreCase("Superior")) {
            phong.setMaLoai("SUP");
        } else if (lp.equalsIgnoreCase("Suite")) {
            phong.setMaLoai("SUT");
        }
        phong.setDonGia(Double.parseDouble(txtGiaThueNgay.getText()));
        phong.setDonGiaTheoGio(Double.parseDouble(txtGiaThueNgay.getText()));
        phong.setGhiChu(txtGhiChu.getText());
        if (pd.capNhatPhong(phong) > 0) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công");
            fillToTable();
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
        }
    }

