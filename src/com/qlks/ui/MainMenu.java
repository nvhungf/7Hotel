    private void fillToTable() {
        PhongDao phDao = new PhongDao();
        DefaultTableModel model = (DefaultTableModel) tblPhong.getModel();
        DefaultTableModel model1 = (DefaultTableModel) tblPhongtrong.getModel();
        DefaultTableModel model2 = (DefaultTableModel) tblPhongdathue.getModel();
        model.setRowCount(0);
        model1.setRowCount(0);
        model2.setRowCount(0);
        try {
            ArrayList<Phong> dsph = phDao.docPhong();
            for (Phong ph : dsph) {
                String ml = ph.getMaLoai().trim();
                String ml2 = "";
                if (ml.equalsIgnoreCase("DLX")) {
                    ml2 = "Deluxe";
                } else if (ml.equalsIgnoreCase("STD")) {
                    ml2 = "Standard";
                } else if (ml.equalsIgnoreCase("SUP")) {
                    ml2 = "Superior";
                } else if (ml.equalsIgnoreCase("SUT")) {
                    ml2 = "Suite";
                }
                Object[] row = {ph.getMaPhong(), ml2, ph.getDonGia(),
                    ph.getDonGiaTheoGio(), ph.getGhiChu()
                };
                model.addRow(row);
            }
            for (Phong ph : dsph) {
                String tt = "";
                tt = ph.getGhiChu().trim();
                if (tt.equalsIgnoreCase("Đã thuê")) {
                    Object[] row2 = {ph.getMaPhong(), ph.getDonGia(), ph.getDonGiaTheoGio(), ph.getMaLoai()
                    };
                    model2.addRow(row2);
                } else {
                    Object[] row1 = {ph.getMaPhong(), ph.getDonGia(), ph.getDonGiaTheoGio(), ph.getMaLoai()
                    };
                    model1.addRow(row1);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu");
        }
    }

